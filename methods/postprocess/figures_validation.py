    print(f"[validate] focal picks ({len(args.picks)}): {args.picks}")
    print(f"[validate] policies ({len(args.policies)}): {args.policies}")
    _param_modes = _parametric_modes_to_run()
    print(f"[validate] parametric Pywr flow mode(s) to run: {_param_modes} (resolved active: {_resolved_pywr_flow_mode()!r})")
    _fpm = _resolved_pywr_flow_mode()
