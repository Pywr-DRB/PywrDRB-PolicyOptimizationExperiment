        if args.plot_start is not None and args.plot_end is not None:
            PLOT_START, PLOT_END = args.plot_start, args.plot_end
            print(f"[plot-window] {res}: {PLOT_START} → {PLOT_END} (--plot-start/--plot-end)")
        elif args.use_opt_window:
            PLOT_START, PLOT_END = _opt_window_for_res(res)
            print(f"[plot-window] {res}: {PLOT_START} → {PLOT_END} (optimization window)")
        else:
            PLOT_START, PLOT_END = args.val_start, args.val_end
            print(f"[plot-window] {res}: {PLOT_START} → {PLOT_END} (--val-start/--val-end)")
