def run_pywr_default(start, end, inflow_type, work_dir, flow_prediction_mode: str | None = None):
    """Build & run default model. Returns (dfD_R_dict, dfD_S_dict) for all reservoirs or (None, None) on failure."""
    if flow_prediction_mode is None:
        flow_prediction_mode = _default_pywr_flow_mode()
    try:
        os.makedirs(work_dir, exist_ok=True)
        mb_def = pywrdrb.ModelBuilder(
            start_date=start,
            end_date=end,
            inflow_type=inflow_type,
            options={"flow_prediction_mode": flow_prediction_mode},
        )
        mb_def.make_model()
        model_def_json = os.path.join(work_dir, "model_default.json")
        mb_def.write_model(model_def_json)
        model_def = pywrdrb.Model.load(model_def_json)
        h5_def = os.path.join(work_dir, "output_default.hdf5")
        _ = pywrdrb.OutputRecorder(model_def, h5_def)
        _ = model_def.run()

        results_sets = ["major_flow", "res_storage", "reservoir_downstream_gage", "lower_basin_mrf_contributions"]
        dataD = pywrdrb.Data(print_status=False, results_sets=results_sets, output_filenames=[h5_def])
        dataD.load_output()
        with warnings.catch_warnings():
            warnings.filterwarnings(
                "ignore",
                message="Resampling with a PeriodIndex is deprecated.*",
                category=FutureWarning,
            )
            dataD.load_observations()
        kD = os.path.splitext(os.path.basename(h5_def))[0]

        dfD_R = {
            res: dataD.reservoir_downstream_gage[kD][0][res].astype(float).rename("default_release")
            for res in reservoir_options
            if res in dataD.reservoir_downstream_gage[kD][0].columns
        }
        dfD_S = {
            res: dataD.res_storage[kD][0][res].astype(float).rename("default_storage")
            for res in reservoir_options
            if res in dataD.res_storage[kD][0].columns
        }
        dfD_T = (
            dataD.major_flow[kD][0]["delTrenton"].astype(float).rename("default_trenton")
            if "delTrenton" in dataD.major_flow[kD][0].columns
            else None
        )
        dfD_MRF = dataD.lower_basin_mrf_contributions[kD][0].copy() if kD in dataD.lower_basin_mrf_contributions else None
        return dfD_R, dfD_S, dfD_T, dfD_MRF
    except Exception as e:
        print(f"[WARN] Default Pywr run failed; continuing without default series. Reason: {e}")
        return None, None, None, None


