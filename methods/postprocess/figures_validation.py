def build_release_policy_dict_and_row_ids(
    sol_vars,
    sol_objs,
    cand_maps,
    reservoirs,
    pol,
    pick,
    require_all_reservoirs: bool = True,
):
    """
    Build ``release_policy_dict`` for *all* ``reservoirs`` for one (policy, pick), plus Borg row
    labels / ilocs for metadata (JSON + HDF5 attrs).

    Returns ``(release_policy_dict, row_index_labels_by_reservoir, row_iloc_by_reservoir)``.
    When ``require_all_reservoirs`` is True, returns ``(None, None, None)`` if any
    requested reservoir lacks this pick. When False, includes only available reservoirs
    and returns ``(None, None, None)`` only if none are available.
    """
    release_policy_dict = {}
    row_labels: dict = {}
    row_ilocs: dict = {}
    for res in reservoirs:
        if res not in sol_objs or pol not in sol_objs.get(res, {}):
            if require_all_reservoirs:
                return None, None, None
            continue
        idxs = get_pick_indices(sol_objs, cand_maps, res, pol, pick)
        if not idxs:
            if require_all_reservoirs:
                return None, None, None
            continue
        var_df = sol_vars[res][pol]
        idx0 = idxs[0]
        params = var_df.loc[idx0].values
        release_policy_dict[res] = {
            "class_type": "ParametricReservoirRelease",
            "policy_type": pol,
            "policy_id": "inline",
            "params": ",".join(str(x) for x in np.asarray(params, float).tolist()),
        }
        row_labels[res] = normalize_borg_row_label(idx0)
        try:
            loc = var_df.index.get_loc(idx0)
            if isinstance(loc, slice):
                row_ilocs[res] = int(loc.start)
            elif isinstance(loc, (np.ndarray, list)):
                row_ilocs[res] = int(loc[0]) if len(loc) else None
            else:
                row_ilocs[res] = int(loc)
        except Exception:
            pass
    if not release_policy_dict:
        return None, None, None
    return release_policy_dict, row_labels, row_ilocs


def build_release_policy_dict_for_pick(sol_vars, sol_objs, cand_maps, reservoirs, pol, pick):
    """
    Build ``release_policy_dict`` for *all* ``reservoirs`` for one (policy, pick), matching stage1's
    combined Pywr run (FE Walter, Prompton, Beltzville, Blue Marsh, … in one model).
    Returns None if any reservoir lacks that pick.
    """
    rdict, _, _ = build_release_policy_dict_and_row_ids(sol_vars, sol_objs, cand_maps, reservoirs, pol, pick)
    return rdict


