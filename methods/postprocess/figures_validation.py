def _resolve_pywr_simulation_dates(
    inflow_type: str,
    pywr_start: str | None,
    pywr_end: str | None,
) -> tuple[str, str]:
    """
    If both ``pywr_start`` and ``pywr_end`` are omitted, use ``model_date_ranges``
    for ``inflow_type`` (full span packaged for that dataset). If both are given,
    use them. If only one is given, exit with an error.
    """
    if pywr_start is None and pywr_end is None:
        if inflow_type not in model_date_ranges:
            raise KeyError(
                f"Unknown --pywr-inflow-type={inflow_type!r}; "
                f"known keys include: {sorted(model_date_ranges)}"
            )
        s, e = model_date_ranges[inflow_type]
        return str(s), str(e)
    if pywr_start is None or pywr_end is None:
        raise SystemExit(
            "Provide both --pywr-start and --pywr-end, or omit both to use "
            "pywrdrb.utils.dates.model_date_ranges for --pywr-inflow-type."
        )
    return pywr_start, pywr_end


